// Subtraction Script
// This script takes two user inputs and then subtracts them.
// and outputs the result to the console. The final result is stored in memory address 50.
// The code uses input (INP), subtraction (SUB), storing values (STA), output (OUT),
// and halting the program (HLT) instructions to perform the subtraction operation.
// Date; Nov 11th 2023
// Created by; yesha

INP  // Take the first user input
STA 50  // Store the user input in Memory Address 50
INP  // Take the second user input
SUB 50  // Call the input in Memory Address 50 [the first input], and subtract it from the second input
STA 50  // Store the answer in Memory Adress 50
OUT  // Output the result
HLT  // Stop the program

// End of Subtraction Program
